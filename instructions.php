<div>
    <h3 style="text-align: center">How to Use the Data Explorer</h3>
    <h4>1. Select the question you want to examine</h4>
    <ul>
        <li>In the first row, click the <span class="bluetext">Select a question</span> drop-down to view all questions. You can type in this box to search through the list.</li>
            <ul><li>For example, type 'marijuana' to show only questions containing that word.</li></ul>
        <li>You can also filter the questions list by selecting a category in the <span class="bluetext">All categories</span> drop-down.</li>
            <ul><li>Select 'Substance Use' to show only questions related to that topic.</li></ul>
        <li>After selecting a question, click <span class="bluetext">Generate Graph</span> to create your custom graph and data table.</li>
    </ul>
    <h4>2. (Optional) Select a second question to "crosstabulate" the two questions</h4>
    <ul>
        <li>Crosstabulate means that responses to the first question will be grouped by responses to the second question.</li>
        <li>For example, if you choose "Online sports betting" and "Gender", the graph will show sports betting data for men (in blue bars) and separately sports betting data for women (in green bars).</li>
    </ul>
    <h4>3. (Optional) Filter data</h4>
    <ul>
        <li>If you set one or more filters, only the respondents that match those filters will be included.</li>
        <li>For example, if you filter by Gender=Male, only male respondents will be included.</li>
    </ul>
</div>

